class ValidationMessages():
  @staticmethod
  def display_marker_validation_msg():
    print('Please enter a valid marker (x/o)')

  @staticmethod
  def display_invalid_input_msg():
    print('Please enter a valid input')

  @staticmethod
  def display_name_validation_msg():
    print('Please enter a valid name')

  @staticmethod
  def display_name_taken_msg():
    print('Name is already taken')

  @staticmethod
  def display_marker_taken_msg():
    print('Marker is already taken!')

  @staticmethod
  def display_board_taken_msg():
    print('This board is already taken!')